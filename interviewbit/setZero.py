class Solution:
	def setZero(self, A):
		rows = []
		cols = []
		for i in xrange(len(A)):
			for j in xrange(len(A[0])):
				if A[i][j] == 0:
					rows.append(i)
					cols.append(j)
		rows = list(set(rows))
		cols = list(set(cols))
		for i in rows:
			for j in xrange(len(A[0])):
				A[i][j] = 0

		for j in cols:
			for i in xrange(len(A)):
				A[i][j] = 0

		return A


